COMMENT ON COLUMN CATEGORY.MAIN_CATEGORY_NO IS '대분류 번호';
COMMENT ON COLUMN CATEGORY.SUB_CATEGORY_NO IS '소분류 번호';
COMMENT ON COLUMN CATEGORY.MAIN_CATEGORY IS '대분류 이름';
COMMENT ON COLUMN CATEGORY.SUB_CATEGORY IS '소분류 이름';

-- AD_MEMBER(관리자) 테이블
