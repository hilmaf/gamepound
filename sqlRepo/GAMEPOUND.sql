-- MEMBER(회원) 테이블
DROP TABLE MEMBER CASCADE CONSTRAINTS;
CREATE TABLE MEMBER (
	NO	                NUMBER	            PRIMARY KEY
	, NAME	            VARCHAR2(42)	    NOT NULL
	, EMAIL	            VARCHAR2(150)	    NOT NULL        UNIQUE
	, PWD	            VARCHAR2(50)	    NOT NULL
	, PIC	            VARCHAR2(500)	    NULL
	, INTRO	            VARCHAR2(2500)	    NULL
	, SITE_URL	        VARCHAR2(500)	    NULL
	, ENROLL_DATE	    TIMESTAMP	        DEFAULT SYSDATE
	, UPDATE_DATE	    TIMESTAMP	        NULL
	, QUIT_YN	        CHAR(1)	            DEFAULT 'N' CHECK( QUIT_YN IN ('Y','N') )
);
COMMENT ON COLUMN MEMBER.NO IS '회원번호';
COMMENT ON COLUMN MEMBER.NAME IS '회원 이름 기본/변경가능';
COMMENT ON COLUMN MEMBER.EMAIL IS '회원 이메일(아이디)';
COMMENT ON COLUMN MEMBER.PWD IS '회원 비밀번호';
COMMENT ON COLUMN MEMBER.PIC IS '회원 프로필사진';
COMMENT ON COLUMN MEMBER.INTRO IS '회원 소개';
COMMENT ON COLUMN MEMBER.SITE_URL IS '회원 웹사이트';
COMMENT ON COLUMN MEMBER.ENROLL_DATE IS '회원 가입일자';
COMMENT ON COLUMN MEMBER.UPDATE_DATE IS '회원 정보 수정일자';
COMMENT ON COLUMN MEMBER.QUIT_YN IS '회원 탈퇴 여부';

-- CATEGORY(카테고리) 테이블
